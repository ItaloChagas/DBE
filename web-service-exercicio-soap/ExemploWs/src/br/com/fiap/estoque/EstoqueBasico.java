package br.com.fiap.estoque;

public class EstoqueBasico {
	
	public int soma(int nr1, int nr2) {
		return (nr1+nr2);
	}
	
	public int subtrai(int nr1, int nr2) {
		return (nr1-nr2);
	}
	
	
	public int multiplica(int nr1, int nr2) {
		return (nr1*nr2);
	}
	
	
	public int divide(int nr1, int nr2) {
		return (nr1/nr2);
	}
	
	
}
